# Assignment Operations

# Leftward assignment
input <- 5
print(paste("Leftward assignment (<-):",input))

# Leftward assignment - not recommended
input  = 6
print(paste("Leftward assignment (=):",input))

# Rightward assignment
8  -> input
print(paste("Rightward assignment (->):",input))

# Global assignment
input  <<- 10
print(paste("Global assignment (<<-):",input))
